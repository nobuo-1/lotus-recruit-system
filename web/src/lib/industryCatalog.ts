// 平坦化した小分類のセット（バリデーション用）
export const INDUSTRY_SMALL_SET = new Set(
  Object.values(INDUSTRY_CATEGORIES).flat()
);

// バリデーション
export function isValidIndustryPair(
  large: string | undefined,
  small: string | undefined
): large is IndustryLarge {
  if (!large || !small) return false;
  if (!INDUSTRY_LARGE.includes(large as IndustryLarge)) return false;
  const list = INDUSTRY_CATEGORIES[large as IndustryLarge] || [];
  return list.includes(small);
}
